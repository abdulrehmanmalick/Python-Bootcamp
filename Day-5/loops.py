# For Loop
fruits = ['Apple', 'Peach', 'Pair']

for fruit in fruits:
    print(fruit)
    print(fruit + ' Pie')

print('\n')

# For Loop With Range
for number in range(1,10):
    print(number)

print('\n')

for number in range(1,10,3):
    print(number)

print('\n')


total = 0
for number in range(1,100):
    total += number
print(total)



